    /**
     * 公共方法  解析激活码
     * @param liense
     * @return
     */
    public static String checkActiveCode(String liense){

        try{
            if(StringUtils.isBlank(liense)){
                return "";
            }else{
                String beforeCode = liense.substring(0, liense.length() - 4);//
                String crc = CRCCheck.Make_CRC(beforeCode.getBytes());
                String afterCode = liense.substring(liense.length() - 4, liense.length());//校验码
                if (!crc.equals(afterCode)){
                    /**
                     * 校验码不对
                     */
                    return "-1";
                }
                byte[] encodedData = Base64Utils.toByteArray(beforeCode);
                byte[] decodedData = decryptByPrivateKey(encodedData);
                /**
                 * 明文格式：6D73FCF4C62536F6C7B2CCC3239AD94B##34##2019-09-01##2019-08-09##34##uuid
                 */
                String target = new String(decodedData);
//                //logger.info("激活码：" + target);
                //获取当前机器码
                String cpu = ComputerUtil.getCpu();
//                //logger.info("cpu：" + cpu);
                String disk = ComputerUtil.getDisk();
//                //logger.info("disk：" + disk);
                String sn = ComputerUtil.getMotherboardSN();
//                //logger.info("sn：" + sn);
                String result = cpu  + "_" + sn + "_" + disk;
//                //logger.info("未加密的原生机器码：" + result);
                String pass = ComputerUtil.MD5(result);
//                //logger.info("加密的机器码：" + pass);
                //分段获取明文的数据
                String[] strs = target.split("##");
                if (pass.equals(strs[0])) {
                    return target;
                }else{
                    /**
                     * 与本机机器码不对
                     */
                    return "-3";
                }
            }
        }catch (Exception e){
            /**
             * 出现异常
             */
            return "-2";
        }
    }


    /**
     * Spring 提供的 PropertiesLoaderUtils 允许您直接通过基于类路径的文件地址加载属性资源
     * 最大的好处就是：实时加载配置文件，修改后立即生效，不必重启
     */
    public static String springUtil(String code){

        Map<String, Object> map = new HashMap<>();
        Properties props = new Properties();
        String privateKey ="密钥获取失败";
        boolean flag =  true;
        while(flag){
            try {
                props=PropertiesLoaderUtils.loadAllProperties("key.properties");
                for(Object key:props.keySet()){
                    map.put(key.toString(),props.get(key).toString());
                }
                flag = false;

            } catch (IOException e) {
                logger.error(e.getMessage());
            }

        }
        privateKey = map.get(code).toString();
        return privateKey;
    }

